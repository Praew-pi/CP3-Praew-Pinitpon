def Plus(x,y):
    print(x, "+", y, "=", x+y)
def Minus(x,y):
    print(x, "-", y, "=", x-y)
def Muliplied(x,y):
    print(x, "x", y, "=", x*y)
def diivide(x,y):
    print(x, "/", y, "=", x/y)

x = int(input("Please input x:"))
y = int(input("Please input y:"))
Plus(x,y)
Minus(x,y)
Muliplied(x,y)
diivide(x,y)